        txtbuscararticulo.addKeyListener(new KeyAdapter() {

            @Override
            public void keyReleased(KeyEvent ke) {
                trs.setRowFilter(RowFilter.regexFilter("(?i)" + txtbuscararticulo.getText(), 1));
            }
        });
        trs = new TableRowSorter(tblregistrarinventario.getModel());
        tblregistrarinventario.setRowSorter(trs);
